package UD04;

public class Ejercicio2 {

	public static void main(String[] args) {
		
		/*Declaro el tipo y el valor de las variables*/
		int n=5;
		double a=3.21;
		char c='g';
		int valorC = (int)c;
		
		/*Output del valor de las variables*/
		System.out.println("Valor n="+n+" Valor a="+a+" Valor c="+c );
		/*Output de la suma de las variables*/
		System.out.println("N + A = "+(n+a));
		/*Output de la resta de las variables*/
		System.out.println("N - A = "+(n-a));
		/*Output del valor numerico de la variable char*/
		System.out.println("Valor numérico del carácter g = "+valorC);
	}

}

package UD04;

public class Ejercicio1 {

	public static void main(String[] args) {
		/*Declaramos el tipo de variables y su valor*/
		int a=40;
		int b=15;
		int resultado;
		
		/*Asignamos el valor de resultado aplicando directamente la parte logica(operación) deseada
		  en este caso Suma*/
		resultado=a+b;
		System.out.println(resultado);
		
		/*Resta*/
		resultado=a-b;
		System.out.println(resultado);
		
		/*Multiplicación*/
		resultado=a*b;
		System.out.println(resultado);
		
		/*División*/
		resultado=a/b;
		System.out.println(resultado);
		
		/*Modulo*/
		resultado=a%b;
		System.out.println(resultado);
	}

}
